      La muestra itinerante "Berni para niños. Las infancias que vio Berni" continúa su gira por todo el territorio provincial, luego de haber pasado por la ciudad que lo vió nacer, Rosario, en el marco de los festejos del día de la bandera, dónde miles y miles de familias la visitaron en el galpón 17.
