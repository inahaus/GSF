        <div class="headline">
          <p>El 13, 14 y 15 de septiembre de 2013 se realizó el cuarto encuentro del programa “Querer, Creer, Crear” en la ciudad de Rosario. El evento se llevó a cabo en el Club Atalaya (Juan Manuel de Rosas 2555) de dicha ciudad.</p>
        </div>


        <p>          De la jornada de clausara participaron la ministra de Innovación y Cultura, María de los Ángeles "Chiqui" González y el secretario de Cultura y Educación de la Municipalidad de Rosario, Horacio Ríos, junto a autoridades provinciales y municipales.
</p>
<p>Durante los días del encuentro 800 músicos, bailarines, artesanos y emprendedores de los barrios República de la Sexta, Tablada y Grandoli de Rosario dieron a conocer sus creaciones.</p>

<p>La carpa especialmente armada para el evento que se instaló sobre calle Juan Manuel de Rosas a la altura del club, albergó a más de 100 artesanos y emprendedores que exhibieron su arte en tejidos, cueros y madera, entre otras creaciones; así como gran variedad de especialidades gastronómicas.</p>




