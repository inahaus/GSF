      <p>Estuvieron presentes también la Biblioteca Popular Pocho Lepratti y FM La Hormiga realizó transmisión en vivo desde la carpa.</p>


      <p>La ministra González contagiada del entusiasmo de los participantes manifestó: “La cultura son ustedes con sus hijos, sus sueños, sus expresiones, su creatividad, los emprendedores, los artesanos. La cultura son los relatos, los nombres, nuestros amores y nuestros abrazos. “Hagamos una cultura que tenga la proximidad del afecto, porque sin afecto no podemos vivir” y dirigiéndose a expositores y artistas les dijo: “Por todo esto les digo gracias”.</p>

      <p>Sobre el escenario del Club Atalaya durante los tres días que duró el encuentro, bailarines y músicos dieron a conocer su talento. Allí convivieron variados ritmos musicales, entre ellos, cantantes melódicos, de tango, bandas de rock, hip hop, junto a un trío que interpretó música de la edad media.</p>

      <p>Acompañaron la actividad el ministro de Salud, Miguel Angel Capiello, el secretario de Programación Sociocultural y Educativa del Ministerio de Innovación y Cultura, Alejandro Tejeda, el director provincial de Programación Territorial, Jorge Zanuzzi y demás miembros del ministerio, junto a la presidenta del Club Atalaya, Sol Nieto, e integrantes de la comisión directiva.</p>
      <p><b>Maestros de la vida</b></p>

      <p>Un momento emotivo del encuentro fue el homenaje a los "Maestros de la Vida". El mismo consistió en un sentido tributo a personalidades selectas por los mismos vecinos en tanto reconocimiento por su contribución y su quehacer edificante en el vecindario.</p>

      <p>La titular de la cartera de cultura destacó lo especialmente significativo de este acto ya que la actual presidenta del Club Atalaya, Sol Nieto, fue la encargada de entregarle la distinción a uno de los dirigentes emblemáticos del club, Alberto Ocanto y dijo al respecto: “Es un momento muy especial ver que Sol, de tan solo 28 años, presidenta de este Club Atalaya, le entregue a Alberto Ocanto el premio Maestro de la Vida. Esto quiere decir que algo se ha sembrado, que la vida continúa, que ha pasado de generación en generación. Que estamos vivos y este es el sentido que tiene este programa” y agregó: Qué importante ir dejándole a los que vienen una posta y decirles este es tu club, este es tu lugar”.</p>

      <p>En esta ocasión los premiados fueron:</p>

      <p>•  Carlos Nuñez, director de la Biblioteca Popular Pocho Lepratti</p>
      <p>•  Carlos Goldbel, director de la Orquesta Infantil y Juvenil de Tablada</p>
      <p>•  Elvio Carbajales y Daniel Filippelli, del Club Temperley</p>
      <p>•  Norma Muiña, del Club Hertz</p>
      <p>•  Alberto Ocanto, del Club Atalaya</p>
      <p>•  Salvador Pagliarini, del Club Calzada Oeste</p>

